#include "libft.h"

void	bzero(void *s, size_t n)
{
	int		cnt;
	char	*temp;

	cnt = 0;
	temp = (char *)s;
	if (n != 0)
	{
		while (cnt < n)
		{
			temp[cnt] = 0;
			cnt++;
		}
	}
}
