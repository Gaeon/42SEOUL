#include "libft.h"

void	*ft_memchr(const char *s, int c, size_t n)
{
	while (*s != '\0' && n > 0)
	{
		if (*s == c)
			return ((void *)s);
		s++;
		n--;
	}
	if (c == '\0' && *s == '\0')
		return ((void *)s);
	return (0);
}
